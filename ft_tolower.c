#include <stdio.h>

int ft_tolower(int c)
{
    if ((c >= 'A') && (c <= 'Z'))
    {
        c += 32; 
    }
    return (c);
}

int main(void)
{
    int c = '&';

    printf("%c \n", ft_tolower(c));
    return (0);
}